# ============================================================================
# CLI Interface
# ============================================================================

def main():
    import argparse
    
    parser = argparse.ArgumentParser(description="Generate tests from rubric criteria")
    parser.add_argument("code_file", help="File containing code to test")
    parser.add_argument("rubric_file", help="JSON file containing rubric")
    parser.add_argument("--output", "-o", help="Output test file path")
    parser.add_argument("--language", "-l", help="Target language")
    parser.add_argument("--framework", "-f", help="Test framework")
    
    args = parser.parse_args()
    
    # Load inputs
    with open(args.code_file) as f:
        code = f.read()
    
    with open(args.rubric_file) as f:
        rubric = json.load(f)
    
    # Determine output path
    output = args.output
    if not output:
        base = args.code_file.rsplit('.', 1)[0]
        ext = args.code_file.rsplit('.', 1)[1] if '.' in args.code_file else 'py'
        if ext == 'py':
            output = f"{base}_test.py"
        elif ext in ('ts', 'tsx'):
            output = f"{base}.test.ts"
        elif ext in ('js', 'jsx'):
            output = f"{base}.test.js"
        elif ext == 'go':
            output = f"{base}_test.go"
        else:
            output = f"{base}_test.py"
    
    # Generate tests
    generator = TestGenerator()
    path = generator.generate_test_file(
        code=code,
        rubric=rubric,
        output_path=output,
        language=args.language,
        framework=args.framework
    )
    
    print(f"Generated test file: {path}")
    
    # Show summary
    with open(path) as f:
        content = f.read()
    
    test_count = content.count('def test_') + content.count('it(') + content.count('func Test')
    print(f"Generated {test_count} test(s) from {len(rubric.get('criteria', []))} criteria")


if __name__ == "__main__":
    main()
